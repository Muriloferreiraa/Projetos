public class operadorUnario {
    public static void main(String[] args) {
        int numero = 9;

        numero = - numero;
        System.out.println(numero);

        int numero2 = 9; 
        numero2 = numero2 + 1; // = numero++
        System.out.println(numero2);

        boolean variavel= true;

        variavel = !variavel; 
        
        System.out.println(variavel);

    }
}  
